<h2>What does 'Hoduma' mean?</h2>
<p>The helpdesk application upon which Hoduma is based is called "Huru" which means "free"/"liberated" in Swahili.
So I also decided to use a Swahili word for the fork. My choice was "Hoduma" which means "assistance/support".</p>